# using python chr() function
# this function takes an integer and convert it to ASCII character

numb1 = 65
ASCI = chr(numb1)
print(ASCI)  # the output will be A

# using ord() function
# this function convert an ASCII character to a integer value

ASCI = 'A'
numb1 = ord(ASCI)
print(numb1)  # the output will be 65