# the format method formats the specified values and insert them inside the strings placeholder

# using named indexes
string1 = "My name is {fname} {lname}".format(fname = "Roman", lname = "Humagain")
print(string1)

# using numbered index
String2 ="I am {0} {1} Student".format("Software" , "Engineering")
print(String2)

# using empty placeholder
String3 = "I am from {}, {}".format("Panauti", "Kavre")
print(String3)

