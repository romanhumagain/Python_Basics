# using loop in tuple to access tuple
newTuple = ("roman", "humagain", "pcps college")

# using for loop
for i in range(len(newTuple)):
    print(newTuple[i])

# using while loop
thisTuple = ("Tiger", "Lion", "Cheetah")

i=0  #start
while i<=len(thisTuple):   # condition
    print(thisTuple[i])
    i+=1 # increment
