# Comparison operators are used to compare two values:

# Equal to:
Num1 = 5
num2 = 10

print(Num1 == num2)  # Output: False

# Not equal to:
Num1 = 5
num2 = 10

print(Num1 != num2)  # Output: True

# Greater than:
Num1 = 5
num2 = 10

print(num2 > Num1)  # Output: True

# Less than:
Num1 = 5
num2 = 10

print(Num1 < num2)  # Output: True

# Greater than or equal to:
Num1 = 5
num2 = 10

print(num2 >= Num1)  # Output: True

# Less than or equal to:
Num1 = 5
num2 = 10

print(Num1 <= num2)  # Output: True