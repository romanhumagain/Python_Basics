# Simple assignment

num_one = 10  # assigning value 10 to x variable

# Addition assignment:
num_one = 5
num_one += 3  # Equivalent to x = x + 3
print(num_one)  # Output: 8

# Subtraction assignment:
num_one = 10
num_one -= 4  # Equivalent to x = x - 4
print(num_one)  # Output: 6

# Multiplication assignment:
num_one = 3
num_one *= 2  # Equivalent to x = x * 2
print(num_one)  # Output: 6

# Division assignment:
num_one = 10
num_one /= 5  # Equivalent to x = x / 5
print(num_one)  # Output: 2.0

# Modulus assignment:
num_one = 7
num_one %= 3  # Equivalent to x = x % 3
print(num_one)  # Output: 1

