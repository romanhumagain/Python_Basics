# Bitwise AND (&):
num1 = 10  # Binary: 1010
num2 = 6   # Binary: 0110
result = num1 & num2
print(result)  # Output: 2 (Binary: 0010)

# Bitwise OR (|):
num1 = 10  # Binary: 1010
num2 = 6   # Binary: 0110
result = num1 | num2
print(result)  # Output: 14 (Binary: 1110)

# Bitwise XOR (^):
num1 = 10  # Binary: 1010
num2 = 6   # Binary: 0110
result = num1 ^ num2
print(result)  # Output: 12 (Binary: 1100)

# Bitwise NOT (~):
num1 = 10  # Binary: 1010
result = ~num1
print(result)  # Output: -11 (Binary: -1011)

# Bitwise left shift (<<):
num1 = 5   # Binary: 0101
result = num1 << 2
print(result)  # Output: 20 (Binary: 10100)

# Bitwise right shift (>>):
num1 = 20  # Binary: 10100
result = num1 >> 2
print(result)  # Output: 5 (Binary: 0101)