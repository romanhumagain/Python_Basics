# Arithmetic operators are used with numeric values to perform common mathematical operations:

num1 = 12
num2 =5

# using arithmetic operator
print(num1 + num2)  # addition of two number
print(num1 - num2)  # subtraction of two number
print(num1 * num2)  # multiplication of two number
print(num1 / num2)  #division of two number
print(num1 // num2) #floor division
print(num1 ** num2) # Exponentiation