# Declare a string and a list
string = "Hello, world!"
list = [1, 2, 3, 4, 5]

# Membership check using 'in'
print('H' in string)  # True, as 'H' is present in the string

print(6 in list)  # False, as 6 is not present in the list

# Membership check using 'not in'
print('o' not in string)  # False, as 'o' is present in the string

print(3 not in list)  # False, as 3 is present in the list
